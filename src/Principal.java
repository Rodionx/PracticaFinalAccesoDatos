    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        File ficheroXML = seleccionarFichero();
        gesSAX.abrir_XML_SAX(ficheroXML);
        
        if(ficheroXML == null){
            this.infoLabel.setText("Vuelve a seleccionar el fichero");
        }
        else{
            if(gesSAX.abrir_XML_SAX(ficheroXML) == -1){
                this.infoLabel.setText("Error al crear el objeto SAX");
            }else{
                this.infoLabel.setText("Objeto SAX creado");
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

